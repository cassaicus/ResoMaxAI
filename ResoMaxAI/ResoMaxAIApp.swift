import SwiftUI

// @main属性は、このResoMaxAIApp構造体がアプリケーションの開始点であることを示します。
// Swiftはこの属性を持つ型を探し、そこからプログラムを実行します。
@main
// Appプロトコルに準拠することで、SwiftUIアプリケーションとしての基本的な振る舞いを定義します。
// アプリのライフサイクル（起動、バックグラウンドへの移行など）を管理する役割を担います。
struct ResoMaxAIApp: App {
    // bodyプロパティは、アプリケーションのメインとなるUI（シーン）を定義します。
    // このプロパティは計算型プロパティで、Sceneプロトコルに準拠する型を返す必要があります。
    var body: some Scene {
        // WindowGroupは、アプリケーションのメインウィンドウを管理するためのシーンです。
        // macOSでは、ユーザーは複数のウィンドウを開くことができますが、
        // この基本的な設定では、1つのメインウィンドウが作成されます。
        WindowGroup {
            // ContentViewは、ウィンドウ内に表示される最初のビュー（画面）です。
            // アプリケーションが起動すると、このContentViewが表示されます。
            // 提案：将来的に、より複雑な状態管理（例えば、画像処理エンジンのインスタンス共有など）が必要になった場合、
            // ContentView()に.environmentObject()モディファイアを追加して、
            // アプリケーション全体で共有するオブジェクトを渡すことを検討すると良いでしょう。
            ContentView()
        }
    }
}
